    private void jButtonAccederActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAccederActionPerformed
        try {
            Class.forName("sun.jdbc.odbc.JdbcOdbcDriver").newInstance();
            Connection cn = DriverManager.getConnection("jdbc:odbc:Proyecto", "sa", "sasa");

            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery("Select tipo from Usuarios where nombre = '" + txtEmail.getText() + "' and password  = '" + txtPass.getText() + "'");

            if (rs.next()) {

                int tipo = rs.getInt("Tipo");
                if (tipo == 1) {
                    dispose();
                    new Administrador().setVisible(true);
                }

                if (tipo == 2) {
                    dispose();
                    new Tecnico().setVisible(true);
                }
                if (tipo == 3) {
                    dispose();
                    new Usuario().setVisible(true);
                }

            } else {

                JOptionPane.showMessageDialog(null, "No existe usuario, porfovor revise sus datos");
            }

        } catch (Exception e) {
            //JOptionPane.showMessageDialog(null, "Error " + e);
            System.out.println(e);
        }
    }//GEN-LAST:event_jButtonAccederActionPerformed

